#include<iostream>
using namespace std;
int main()
{
    int x = 10,y = 20;
    cout<<(x>y)<<"\n";//is false so output is 0.
    cout<<(x<y)<<"\n";//is true so output is 1.
    cout<<(x>=y)<<"\n";//is false so output is 0.
    cout<<(x<=y)<<"\n";//is true so output is 1.
    cout<<(x==y)<<"\n";//is false so output is 0.
    cout<<(x!=y);//ismtrue so output is 1.
    return 0; 
}